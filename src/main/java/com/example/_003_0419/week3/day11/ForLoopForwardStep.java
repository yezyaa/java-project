package com.example._003_0419.week3.day11;

public class ForLoopForwardStep {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i+=2) {
            System.out.println(i);
        }
        for (int i = 1; i < 10; i+=2) {
            System.out.println(i);
        }
    }
}
