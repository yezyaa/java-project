package com.example._003_0419.codeUp._1000;

import java.util.Scanner;

public class Codeup1073 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(true) {
            int val = sc.nextInt();
            if (val == 0) {
                break;
            }
            System.out.printf("%d\n", val);
        }
    }
}
