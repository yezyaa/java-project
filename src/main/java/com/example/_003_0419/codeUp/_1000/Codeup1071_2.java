package com.example._003_0419.codeUp._1000;

import java.util.Scanner;

public class Codeup1071_2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int input;
        do {
            input = sc.nextInt();
            if (input != 0) {
                System.out.printf("%d\n", input);
            }
        } while (input != 0);
    }
}
