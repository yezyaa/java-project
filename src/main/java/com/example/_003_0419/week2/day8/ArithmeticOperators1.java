package com.example._003_0419.week2.day8;

public class ArithmeticOperators1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        System.out.printf("a + b = %d\n", a + b);
        System.out.printf("a - b = %d\n", a - b);
        System.out.printf("a * b = %d\n", a * b);
        System.out.printf("a / b = %d\n", a / b);
        System.out.printf("a %% b = %d\n", a % b);
    }
}
