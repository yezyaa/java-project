package com.example._003_0419.week2.day9;

public class IncreaseOperator {
    public static void main(String[] args) {
        int i = 10;
        System.out.println("[++가 뒤에 오는 경우]");
        System.out.printf("i: %d\n", i);
        System.out.printf("i++: %d\n", i++);
        System.out.printf("i: %d\n\n", i);
        System.out.println("[++가 앞에 오는 경우]");
        System.out.printf("i: %d\n", i);
        System.out.printf("++i: %d\n", ++i);
    }
}
