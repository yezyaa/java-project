package com.example._003_0419.week2.day9;

public class IncreaseFirst {
    public static void main(String[] args) {
        int idx = 0;
        int[] arr = {2, 1, 7, 4};
        System.out.println(arr[idx]);
        idx += 1;
        System.out.println(arr[idx]);
        idx += 1;
        System.out.println(arr[idx]);
    }
}
